
import java.util.Scanner;

public class HolaMundo {

    public static void main(String args[]) {
        /*
        tipos primitivos enteros: byte, short. int, long
         */
        byte numeroByte = 10;
        short numeroShort = 128;
        int numeroInt = 1000;
        long numeroLong=1000000;
        System.out.println("numeroByte = " + numeroByte);
        System.out.println("numero minimo byte: " + Byte.MIN_VALUE);
        System.out.println("numero maximo byte: " + Byte.MAX_VALUE);
        System.out.println("numeroShort = " + numeroShort);
        System.out.println("numero minimo short: " + Short.MIN_VALUE);
        System.out.println("numero maximo short: " + Short.MAX_VALUE);
        System.out.println("numeroInt = " + numeroInt);
        System.out.println("numero minimo Int = " + Integer.MIN_VALUE);
        System.out.println("numero minimo Int = " + Integer.MAX_VALUE);
        System.out.println("numeroLong = " + numeroLong);
        System.out.println("numero minimo Long = " + Long.MIN_VALUE);
        System.out.println("numero minimo Long = " + Long.MAX_VALUE);

    }
}
